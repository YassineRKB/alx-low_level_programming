/**
* op_add - addition function
* @a: 1st num
* @b: 2nd num
* Return: int result
**/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - subtraction function
* @a: 1st num
* @b: 2nd num
* Return: int result
**/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multiplication function
* @a: 1st num
* @b: 2nd num
* Return: int result
**/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - division function
* @a: 1st num
* @b: 2nd num
* Return: int result
**/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - Modulation function
* @a: 1st num
* @b: 2nd num
* Return: int result
**/
int op_mod(int a, int b)
{
	return (a % b);
}
